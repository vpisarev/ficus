#!/usr/bin/env python3
"""
cfold oracle generator (Python 3, stdlib only).

Emits a self-contained .fx program with N random integer constant expressions,
each evaluated TWICE and asserted bit-equal:

  (a) fold path -- the operands are literals, so the constant folder
      (K_cfold_dealias) computes the result at compile time;
  (b) runtime path -- each operand is routed through a module-level `var`
      (`(lit + Z<T>) :> T`, where Z<T> is set to 0 at startup from argv so the
      optimizer cannot prove it constant), so the operation survives to run time.

The runtime path is the reference (C codegen on the native integer types is
believed correct); a mismatch means the folder disagrees with runtime -- exactly
the FB-002 class (uint64 >> / div / mod / compare done with signed semantics).

Deterministic: values come from a Python splitmix64 seeded by FXTEST_SEED (same
policy as the T5 suites), so the emitted program is fixed for a given seed.
"""

import os

# name, literal-suffix, ficus type, signed?, bits
TYPES = [
    ("int", "",    "int",    True,  64),
    ("i8",  "i8",  "int8",   True,  8),
    ("i16", "i16", "int16",  True,  16),
    ("i32", "i32", "int32",  True,  32),
    ("i64", "i64", "int64",  True,  64),
    ("u8",  "u8",  "uint8",  False, 8),
    ("u16", "u16", "uint16", False, 16),
    ("u32", "u32", "uint32", False, 32),
    ("u64", "u64", "uint64", False, 64),
]
TYPE_BY_NAME = {t[0]: t for t in TYPES}

ARITH = ["+", "-", "*", "&", "|", "^"]
DIVMOD = ["/", "%"]
SHIFT = ["<<", ">>"]
CMP = ["==", "!=", "<", ">", "<=", ">="]


def _mask(bits):
    return (1 << bits) - 1


def _range(signed, bits):
    if signed:
        lo = -(1 << (bits - 1))
        # ficus's typechecker treats the 64-bit signed range as symmetric
        # [-(2^63-1), 2^63-1] (Ast_typecheck.fx); the true INT64_MIN -2^63 is
        # outside the supported *literal* range and its C emission (`-9223...808`
        # without an LL suffix) is a gray area, so do not generate it.
        if bits == 64:
            lo = -(1 << (bits - 1)) + 1
        return lo, (1 << (bits - 1)) - 1
    return 0, (1 << bits) - 1


class SplitMix64:
    def __init__(self, seed):
        self.s = seed & 0xFFFFFFFFFFFFFFFF

    def next(self):
        self.s = (self.s + 0x9E3779B97F4A7C15) & 0xFFFFFFFFFFFFFFFF
        z = self.s
        z = ((z ^ (z >> 30)) * 0xBF58476D1CE4E5B9) & 0xFFFFFFFFFFFFFFFF
        z = ((z ^ (z >> 27)) * 0x94D049BB133111EB) & 0xFFFFFFFFFFFFFFFF
        return z ^ (z >> 31)

    def below(self, n):
        return self.next() % n

    def pick(self, seq):
        return seq[self.below(len(seq))]


def _val(rng, signed, bits):
    """A random value in range, biased toward edge/high-bit cases."""
    lo, hi = _range(signed, bits)
    r = rng.below(8)
    if r == 0:
        return lo
    if r == 1:
        return hi
    if r == 2:
        return 0
    if r == 3 and bits > 1:                 # high bit set (sign bit for signed)
        return (1 << (bits - 1)) if not signed else lo
    if r == 4:
        return hi - 1 if hi > lo else lo
    span = hi - lo + 1
    return lo + (rng.next() % span)


def _lit(v, suffix):
    # ficus literals: negative literals are fine as `-N`; hex not needed.
    return f"{v}{suffix}"


def _opq(v, suffix, tname, tkey):
    # runtime-opaque operand of type `tname`, value v: (lit + Z<tkey>) :> tname
    return f"(({_lit(v, suffix)} + Z{tkey}) :> {tname})"


def generate(seed, n):
    rng = SplitMix64(seed)
    lines = []
    lines.append("// AUTO-GENERATED by tools/fxtest/cfold_gen.py -- do not edit.")
    lines.append(f"// cfold oracle: {n} expressions, seed={seed}.")
    lines.append("import Sys")
    lines.append("var mism = 0")
    for key, suf, tname, signed, bits in TYPES:
        lines.append(f"var Z{key}: {tname} = 0{suf}")
    lines.append("fun init_opq(): void {")
    lines.append("    val z = Sys.argv.length() - 1   // 0 at runtime, opaque to cfold")
    for key, suf, tname, signed, bits in TYPES:
        lines.append(f"    Z{key} = ({tname})(z)")
    lines.append("}")
    lines.append("fun run(): void {")

    def emit_cmp(idx, fold, run, tag):
        lines.append(f"    val f{idx} = {fold}")
        lines.append(f"    val o{idx} = {run}")
        lines.append(f"    if f{idx} != o{idx} {{ mism += 1;")
        lines.append(f"        if mism <= 20 {{ println(f\"MISMATCH {tag} fold={{f{idx}}} run={{o{idx}}}\") }} }}")

    for i in range(n):
        kind = rng.below(100)
        # ~15% shifts, ~15% div/mod, ~20% comparisons, rest arithmetic/bitwise
        key, suf, tname, signed, bits = rng.pick(TYPES)
        if kind < 15:                                   # shift
            op = rng.pick(SHIFT)
            x = _val(rng, signed, bits)
            s = rng.below(bits)                          # shift count in [0,bits)
            fold = f"(({_lit(x, suf)} {op} {s}) :> {tname})"
            run = f"(({_opq(x, suf, tname, key)} {op} (({s} + Zint) :> int)) :> {tname})"
            emit_cmp(i, fold, run, f"t={tname} x={x} {op} {s}")
        elif kind < 30:                                 # div / mod
            op = rng.pick(DIVMOD)
            x = _val(rng, signed, bits)
            y = _val(rng, signed, bits)
            if y == 0:
                y = 1
            if signed and y == -1:                      # avoid MIN / -1 overflow (UB)
                y = 1
            fold = f"({_lit(x, suf)} {op} {_lit(y, suf)})"
            run = f"({_opq(x, suf, tname, key)} {op} {_opq(y, suf, tname, key)})"
            emit_cmp(i, fold, run, f"t={tname} x={x} {op} y={y}")
        elif kind < 50:                                 # comparison
            op = rng.pick(CMP)
            x = _val(rng, signed, bits)
            y = _val(rng, signed, bits)
            fold = f"({_lit(x, suf)} {op} {_lit(y, suf)})"
            run = f"({_opq(x, suf, tname, key)} {op} {_opq(y, suf, tname, key)})"
            emit_cmp(i, fold, run, f"t={tname} x={x} {op} y={y}")
        else:                                           # + - * & | ^
            op = rng.pick(ARITH)
            x = _val(rng, signed, bits)
            y = _val(rng, signed, bits)
            fold = f"(({_lit(x, suf)} {op} {_lit(y, suf)}) :> {tname})"
            run = f"(({_opq(x, suf, tname, key)} {op} {_opq(y, suf, tname, key)}) :> {tname})"
            emit_cmp(i, fold, run, f"t={tname} x={x} {op} y={y}")

    lines.append("}")
    lines.append("init_opq()")
    lines.append("run()")
    lines.append('println(f"cfold-oracle: {mism} mismatches / ' + str(n) + ' expressions")')
    return "\n".join(lines) + "\n"


def base_seed():
    env = os.environ.get("FXTEST_SEED")
    if env:
        try:
            return int(env, 0) & 0xFFFFFFFFFFFFFFFF
        except ValueError:
            pass
    # fixed default so runs are reproducible without an env var
    return 0x1234_5678_9ABC_DEF0
